<?php
	
	// Operadores de Asignación

	/**
	 *	Asignación
	 *	Realizacion la asignación de un valor que esté a la derecha,
	 *	a una variable que esté a la izquierda del operador.
	 */
	$var = 10;

	/**
	 *	Suma-Asignación
	 *	Suma el valor de la derecha al valor de la variable a la izquierda
	 *	y lo asigna en la variable.
	 */
	$var = 10;
	$var += 5; // 15

	/**
	 *	Resta-Asignación
	 *	Resta el valor de la derecha al valor de la variable a la izquierda
	 *	y lo asigna en la variable.
	 */
	$var = 10;
	$var -= 5; // 5

	/**
	 *	Multiplicación-Asignación
	 *	Multiplica el valor de la derecha al valor de la variable a la izquierda
	 *	y lo asigna en la variable.
	 */
	$var = 10;
	$var *= 5; // 50

	/**
	 *	División-Asignación
	 *	Divide el valor de la derecha al valor de la variable a la izquierda
	 *	y lo asigna en la variable.
	 */
	$var = 10;
	$var /= 5; // 2

	/**
	 *	Módulo-Asignación
	 *	Calcula el módulo del valor de la derecha y el valor de la variable a la izquierda
	 *	y lo asigna en la variable.
	 */
	$var = 10;
	$var %= 5; // 0

	/**
	 *	Concatenación-Asignación
	 *	Concatena el valor de la derecha y el valor de la variable a la izquierda
	 *	y lo asigna en la variable.
	 */
	$var = 'Hola ';
	$var .= 'Mundo'; // Hola Mundo

?>