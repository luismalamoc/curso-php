	/** 
	 *	Igual A 
	 *	Retorna True si el primer operando es igual al segundo.
	 */
	$var = 1 == 1; // True

	/**
	 *	Diferente A
	 *	Retorna True si el primer operando es diferente al segundo.
	 */
	$var = 1 != 1; // False 
	$var = 1 <> 2; // True

	/**
	 *  Igual A
	 *	Retorna True si el primer operando es igual al segundo en
	 *	valor y tipo de dato.
	 */
	$var = 1 === 1; // True

	/**
	 *  Diferente A
	 *	Retorna True si el primer operando no es igual al segundo en
	 *	valor y tipo de dato.
	 */
	$var = 1 !== '1'; // False

	/**
	 *	Menor Que
	 *	Retorna True si el primer operando es menor que el segundo.
	 */
	$var = 1 < 2; // True

	/**
	 *	Mayor Que
	 *	Retorna True si el primer operando es mayor que el segundo.
	 */
	$var = 1 > 2; // False

	/**
	 *	Menor o Igual Que
	 *	Retorna True si el primer operando es menor o igual que el segundo.
	 */
	$var = 1 <= 1; // True

	/**
	 *	Mayor o Igual Que
	 *	Retorna True si el primer operando es mayor o igual que el segundo.
	 */
	$var = 1 >= 2; // False
	
