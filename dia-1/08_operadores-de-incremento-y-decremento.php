<?php
	
	// Operadores de incremento y decremento
	$variable1 = 100;
	$variable2 = 100;
	$variable3 = 100;
	$variable4 = 100;
	echo $variable1 ++ ; // Esto imprimirá 100 y luego incrementará la variable en uno.
	echo $variable1;
	echo $variable2 --; // Esto imprimirá 100 y luego restará uno a la variable.
	echo $variable2;
	echo ++ $variable3; // Aquí se incrementa uno primero y luego se imprime. 
	echo $variable3;
	echo -- $variable4; // Se resta primero uno y luego se imprime.
	echo $variable4;
	
?>